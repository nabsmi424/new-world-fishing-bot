# Python program to demonstrate to
# sorting numbers in Ascending Order

numbers = [1, 3, 4, 2]
# Sorting list of Integers in ascending
print(numbers.sort()) # None
print(numbers)		 # [1, 2, 3, 4]

print(sorted(numbers)) # [1, 2, 3, 4]
print(numbers)		 # [1, 3, 4, 2]

